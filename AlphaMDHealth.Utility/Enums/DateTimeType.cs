namespace AlphaMDHealth.Utility;

/// <summary>
/// Specify The DateTime Type for Control
/// </summary>
public enum DateTimeType
{
    /// <summary>
    /// DateTime Control with Type "DateTime"
    /// </summary>
    DateTime,

    /// <summary>
    /// DateTime Control with Type "Date"
    /// </summary>
    Date,

    /// <summary>
    /// DateTime Control with Type "Time"
    /// </summary>
    Time,

    /// <summary>
    /// DateTime Control with Type "MonthYear"
    /// </summary>
    MonthYear,

    /// <summary>
    /// DateTime Control with Type "ExtendedDateTime"
    /// </summary>
    ExtendedDate,

    /// <summary>
    /// DateTime Control with Type "ExtendedDateTime"
    /// </summary>
    ExtendedDateTime,

    /// <summary>
    /// DateTime format for DayMonth
    /// </summary>
    DayMonth,

    /// <summary>
    /// DateTime format for Hour only
    /// </summary>
    Hour,

    /// <summary>
    /// DateTime format for day only
    /// </summary>
    Day
}